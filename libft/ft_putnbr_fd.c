#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int	why;

	if (n == -2147483648)
	{
		why = write(fd, "-2147483648", 11);
		why++;
		return ;
	}
	if (n < 0)
	{
		why = write(fd, "-", 1);
		why++;
		n *= -1;
	}
	if (n >= 10)
		ft_putnbr_fd(n / 10, fd);
	ft_putchar_fd(n % 10 + '0', fd);
}
